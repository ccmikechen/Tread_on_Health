	synchronized public static ShoePoint getCenterOfPressurePoint(
			Direction direction, float a, float b, float c, float d) {

		double xp = direction == Direction.LEFT ?
				leftSensorPoint[0].x*a + leftSensorPoint[1].x*b + leftSensorPoint[2].x*c + leftSensorPoint[3].x*d :
				rightSensorPoint[0].x*a + rightSensorPoint[1].x*b + rightSensorPoint[2].x*c + rightSensorPoint[3].x*d;
		
		double yp = direction == Direction.LEFT ? 
				leftSensorPoint[0].y*a + leftSensorPoint[1].y*b + leftSensorPoint[2].y*c + leftSensorPoint[3].y*d :
				rightSensorPoint[0].y*a + rightSensorPoint[1].y*b + rightSensorPoint[2].y*c + rightSensorPoint[3].y*d;
				
		double p = a + b + c + d;
		return new ShoePoint((float) (xp / p), (float) (yp / p));
		
	}
